# lib/i18n.sh - tiny message catalog for awg2 (English/Русский/中文/Tiếng Việt).
# Only PROSE is translated; commands, paths, vless://, config keys stay literal
# (passed as %s args). Language: --lang=xx > RVLANG > $LANG > en.
#
#   i18n_load            # after RVLANG/LANG known
#   t key [printf-args]  # -> localized string (no trailing newline)

declare -Ag I18N

norm_lang() {
    case "$(printf '%s' "${1:-}" | tr 'A-Z' 'a-z')" in
        ru|ru[_-]*|russian) echo ru ;;
        zh|zh[_-]*|cn|chinese) echo zh ;;
        vi|vi[_-]*|vn|vietnamese) echo vi ;;
        *) echo en ;;
    esac
}

t() {
    local k="$1"; shift || true
    local f="${I18N[$k]:-$k}"
    if [ "$#" -eq 0 ]; then printf '%s' "$f"; else printf "$f" "$@"; fi
}

